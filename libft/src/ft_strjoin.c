#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	char	*s3;

	if (!s1 || !s2 || !(s3 = (char *)malloc(sizeof(char) * (ft_strlen(s1) + \
						ft_strlen(s2) + 1))))
		return (NULL);
	i = 0;
	j = ft_strlen(s1);
	while (s1[i])
	{
		s3[i] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		s3[j] = s2[i];
		i++;
		j++;
	}
	s3[j] = '\0';
	return (s3);
}
